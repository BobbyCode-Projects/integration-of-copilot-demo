// Get statistics and chart aggregates
app.get('/api/analytics', (req, res) => {
  const db = getDatabase();
  const tickets = db.tickets;
  const meetings = db.meetings;

  const totalMeetings = meetings.length;
  const tasksGenerated = tickets.length;
  const completedTasks = tickets.filter(t => t.status === 'Completed').length;
  const pendingTasks = tickets.filter(t => t.status !== 'Completed').length;
  
  // Calculate average completion time (mocked, but relative to completed tickets)
  const averageCompletionTime = completedTasks > 0 ? '1.8 Days' : 'N/A';
  
  const highPriorityTasks = tickets.filter(t => t.priority === 'High').length;

  // 1. Task Status Distribution
  const statusCounts = {
    'To Do': tickets.filter(t => t.status === 'To Do').length,
    'In Progress': tickets.filter(t => t.status === 'In Progress').length,
    'Testing': tickets.filter(t => t.status === 'Testing').length,
    'Completed': tickets.filter(t => t.status === 'Completed').length
  };

  // Convert for frontend chart representation
  const statusData = Object.entries(statusCounts).map(([name, value]) => ({ name, value }));

  // 2. Assignee Workload
  const assignees = Array.from(new Set(tickets.map(t => t.assignee)));
  const assigneeData = assignees.map(name => {
    const userTickets = tickets.filter(t => t.assignee === name);
    return {
      name,
      'To Do': userTickets.filter(t => t.status === 'To Do').length,
      'In Progress': userTickets.filter(t => t.status === 'In Progress').length,
      'Testing': userTickets.filter(t => t.status === 'Testing').length,
      'Completed': userTickets.filter(t => t.status === 'Completed').length,
      total: userTickets.length
    };
  });

  // 3. Priority Distribution
  const priorityCounts = {
    'High': tickets.filter(t => t.priority === 'High').length,
    'Medium': tickets.filter(t => t.priority === 'Medium').length,
    'Low': tickets.filter(t => t.priority === 'Low').length
  };
  const priorityData = Object.entries(priorityCounts).map(([name, value]) => ({ name, value }));

  res.json({
    metrics: {
      totalMeetings,
      tasksGenerated,
      completedTasks,
      pendingTasks,
      averageCompletionTime,
      highPriorityTasks
    },
    charts: {
      statusData,
      assigneeData,
      priorityData
    }
  });
});

