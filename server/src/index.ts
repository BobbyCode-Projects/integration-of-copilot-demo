// Create a new meeting (simulating active meeting starting)
app.post('/api/meetings', (req, res) => {
  const db = getDatabase();
  const { title, participants } = req.body;
  
  const newMeeting: Meeting = {
    id: `meet-${Date.now()}`,
    title: title || 'Quick Sync Meeting',
    participants: participants || ['John', 'Sarah', 'Mike'],
    status: 'running',
    transcript: [],
    duration: 0,
    analysis: null,
    createdAt: new Date().toISOString()
  };
  
  db.meetings.unshift(newMeeting);
  saveDatabase(db);
  res.status(201).json(newMeeting);
});

// Add transcript line to active meeting
app.post('/api/meetings/:id/transcript', (req, res) => {
  const db = getDatabase();
  const { id } = req.params;
  const { speaker, text, timestamp } = req.body;

  const meeting = db.meetings.find(m => m.id === id);
  if (!meeting) {
    return res.status(404).json({ error: 'Meeting not found' });
  }

  if (meeting.status !== 'running') {
    return res.status(400).json({ error: 'Meeting is already ended' });
  }

  meeting.transcript.push({
    speaker,
    text,
    timestamp: timestamp || new Date().toLocaleTimeString([], { hour: '2-digit', minute: '2-digit' })
  });

  saveDatabase(db);
  res.json(meeting);
});

// End active meeting
app.post('/api/meetings/:id/end', (req, res) => {
  const db = getDatabase();
  const { id } = req.params;
  const { duration } = req.body;

  const meeting = db.meetings.find(m => m.id === id);
  if (!meeting) {
    return res.status(404).json({ error: 'Meeting not found' });
  }

  meeting.status = 'ended';
  meeting.duration = duration || Math.floor(Math.random() * 20) + 15; // default 15-35 mins

  // Push activity notification
  db.teamsActivity.unshift({
    id: `act-${Date.now()}`,
    title: 'Meeting Ended',
    description: `Teams meeting "${meeting.title}" completed. Recording and transcript saved.`,
    type: 'meeting_ended',
    timestamp: 'Just now',
    user: 'Teams'
  });

  saveDatabase(db);
  res.json(meeting);
});

// Trigger Copilot analysis
app.post('/api/meetings/:id/analyze', (req, res) => {
  const db = getDatabase();
  const { id } = req.params;

  const meeting = db.meetings.find(m => m.id === id);
  if (!meeting) {
    return res.status(404).json({ error: 'Meeting not found' });
  }

  if (meeting.status !== 'ended') {
    return res.status(400).json({ error: 'Cannot analyze a running meeting. End it first.' });
  }

  // Generate simulated analysis using the aiService Heuristics
  meeting.analysis = analyzeTranscript(meeting.transcript);
  
  saveDatabase(db);
  res.json(meeting);
});

// Commit analyzed tasks into Jira, Slack, Teams
app.post('/api/meetings/:id/sync', (req, res) => {
  const db = getDatabase();
  const { id } = req.params;

  const meeting = db.meetings.find(m => m.id === id);
  if (!meeting) {
    return res.status(404).json({ error: 'Meeting not found' });
  }

  if (!meeting.analysis) {
    return res.status(400).json({ error: 'No Copilot analysis found to sync. Run analysis first.' });
  }

  const { tasks } = meeting.analysis;
  if (tasks.length === 0) {
    return res.json({ message: 'No tasks found to sync.' });
  }

  const createdTickets: JiraTicket[] = [];
  const startId = 100 + db.tickets.length + 1;

  tasks.forEach((task, index) => {
    const ticketId = `JIRA-${startId + index}`;
    
    // Create Jira ticket
    const newTicket: JiraTicket = {
      id: ticketId,
      title: task.title,
      description: `Task automatically generated by Microsoft Copilot from Teams Meeting "${meeting.title}".\nDue by: ${task.dueDate}.`,
      assignee: task.assignee,
      sprint: 'Sprint 1',
      priority: task.priority,
      dueDate: task.dueDate,
      status: task.status,
      createdAt: new Date().toISOString(),
      updatedAt: new Date().toISOString()
    };

    db.tickets.unshift(newTicket);
    createdTickets.push(newTicket);

    // Create Teams Activity Feed item
    db.teamsActivity.unshift({
      id: `act-sync-${Date.now()}-${index}`,
      title: 'New Jira Ticket Created',
      description: `Copilot created ${ticketId} for ${task.assignee}: ${task.title}`,
      type: 'sprint_update',
      timestamp: 'Just now',
      user: 'Copilot'
    });
  });

  // Create Slack channel notification message
  const slackTextLines = [
    `🤖 **Copilot**`,
    `${tasks.length} Jira tasks created successfully for **${meeting.title}**:`
  ];
  tasks.forEach((task, i) => {
    const tId = createdTickets[i]?.id || 'JIRA-XXX';
    slackTextLines.push(`• **${task.assignee}** → ${task.title} [${tId}] (Due ${task.dueDate}, Priority: ${task.priority})`);
  });

  db.slackMessages.push({
    id: `slack-sync-${Date.now()}`,
    sender: '🤖 Copilot',
    text: slackTextLines.join('\n'),
    timestamp: new Date().toLocaleTimeString([], { hour: '2-digit', minute: '2-digit' }),
    channel: '#sprint-automation',
    isBot: true
  });

  saveDatabase(db);
  res.json({
    message: `${tasks.length} tasks synced successfully.`,
    tickets: createdTickets
  });
});

