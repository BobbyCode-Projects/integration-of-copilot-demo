import express from 'express';
import cors from 'cors';
import { 
  getDatabase, 
  saveDatabase, 
  resetDatabase, 
  Meeting, 
  JiraTicket, 
  SlackMessage, 
  TeamsActivity 
} from './db';
import { analyzeTranscript } from './aiService';

const app = express();
const PORT = process.env.PORT || 5001;

app.use(cors());
app.use(express.json());

// Helper to simulate latency
const delay = (ms: number) => new Promise(resolve => setTimeout(resolve, ms));

// Middleware for simulated network latency
app.use(async (req, res, next) => {
  const db = getDatabase();
  const latency = db.settings?.latencyMs || 0;
  if (latency > 0) {
    await delay(latency);
  }
  next();
});

