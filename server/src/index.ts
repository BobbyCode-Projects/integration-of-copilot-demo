// Update a Jira ticket status
app.patch('/api/jira/tickets/:id', (req, res) => {
  const db = getDatabase();
  const { id } = req.params;
  const { status } = req.body;

  const ticket = db.tickets.find(t => t.id === id);
  if (!ticket) {
    return res.status(404).json({ error: 'Ticket not found' });
  }

  const oldStatus = ticket.status;
  if (oldStatus === status) {
    return res.json(ticket);
  }

  ticket.status = status;
  ticket.updatedAt = new Date().toISOString();

  // Create notifications depending on status transitions
  let statusEmoji = '🔄';
  let activityType: TeamsActivity['type'] = 'sprint_update';
  
  if (status === 'In Progress') {
    statusEmoji = '🚀';
    activityType = 'task_start';
  } else if (status === 'Testing') {
    statusEmoji = '🔬';
    activityType = 'task_testing';
  } else if (status === 'Completed') {
    statusEmoji = '✅';
    activityType = 'task_complete';
  }

  // 1. Post to Slack
  db.slackMessages.push({
    id: `slack-update-${Date.now()}`,
    sender: '🤖 Copilot',
    text: `${statusEmoji} Jira ticket **${ticket.id}** (${ticket.title}) was moved to **${status}** by **${ticket.assignee}**.`,
    timestamp: new Date().toLocaleTimeString([], { hour: '2-digit', minute: '2-digit' }),
    channel: '#sprint-automation',
    isBot: true
  });

  // 2. Post to Teams Activity Feed
  db.teamsActivity.unshift({
    id: `teams-update-${Date.now()}`,
    title: `Jira Status Updated`,
    description: `${ticket.assignee} changed ${ticket.id} status to ${status}.`,
    type: activityType,
    timestamp: 'Just now',
    user: ticket.assignee
  });

  saveDatabase(db);
  res.json(ticket);
});

